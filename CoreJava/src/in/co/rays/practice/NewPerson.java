package in.co.rays.practice;

import java.util.Date;

public class NewPerson {
	private String name;
	private int age;
	private Date dob;

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getAge() {
		return age;
	}

	public void setDob(Date dob) {
		this.dob=dob;
	}

	public Date getDob() {
		return dob;
	}

}
